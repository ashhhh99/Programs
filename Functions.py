# Function for converting Fahrenheit to Réaumur.
def FR_converter(temperatureF):
    temperatureR = temperatureF  = (temperatureF  - 32) * 4/9 # Calculation for converting Fahrenheit to Réaumur.
    temperatureR = str(round(temperatureR, 2)) # Rounding the calculations to 2 decimal places.
    print("The temperature in Réaumur is: ", temperatureR) # Printing the converted temprature.

# Main function.
def main():
    temperatureF  = eval(input("Enter temperature in Fahrenheit to be converted to Réaumur: ")) # Requesting input, or temp in farhenheit to be converted to Réaumur
    FR_converter(temperatureF) # calling FR converter function.



# Calling main function
main()
